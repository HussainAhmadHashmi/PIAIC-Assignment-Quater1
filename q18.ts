// Question 18 Make an array with five places and sort in alphabetic and reverse order and also reverse the original array

const vizitPlaces:string[] = ["Madina","Karbla", "Singapur", "Caneda", "Kashmir" ];

// print Original Array 
console.log("Original Array= ",vizitPlaces);

// Print your array in alphabetical order
const alphabeticOrder:string[] = [...vizitPlaces]
console.log("Sorted Array in alphabetical order= ",alphabeticOrder.sort());

// print Original Array 
console.log("Original Array= ",vizitPlaces);

// Print your array in reverse alphabetical order
const reverseOrder:string[] = [...vizitPlaces]
console.log("Sorted Array reverse alphabetical order= ",reverseOrder.sort().reverse());

// print Original Array 
console.log("Original Array= ",vizitPlaces);

// Reverse the order of the original list
vizitPlaces.reverse();
console.log("Original Array Reversed :", vizitPlaces)

// print Original Array 
console.log("Original Array= ",vizitPlaces);











  

  









